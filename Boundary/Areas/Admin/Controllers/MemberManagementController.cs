        public ActionResult PostDelete(long id)
        {
            try
            {
                CheckSessionDataModel checkSession = CheckAdminSession();
                if (!checkSession.IsSuccess)
                {
                    if (checkSession.Message == StaticString.Message_WrongAccess)
                        return RedirectToAction(StaticString.Action_Login, StaticString.Controller_ForLogin, new { area = "" });
                    if (checkSession.Message == StaticString.Message_UnSuccessFull)
                        return RedirectToAction(StaticString.Action_Error, StaticString.Controller_ForError, new { area = "" });
                    return Json(JsonResultHelper.FailedResultWithMessage(checkSession.Message), JsonRequestBehavior.AllowGet);
                }

                DataModel.Entities.Member member = new MemberBL().SelectOne(id);
                var orders = new OrderBL().GetMemberOrders(id,1,1);
                bool result;

                //اگه سفارشی براش ثبت شده باشه، فقط غیر فعالش میکنیم
                if (orders != null && orders.Count > 0)
                {
                    var user = new UserBL().GetById(member.UserCode);
                    user.IsActive = false;
                    result=new UserBL().Update(user).DbMessage.MessageType==MessageType.Success;
                }
                else
                {
                    result=new MemberBL().Delete(id);
                }

                if (result)
                {
                    if (!string.IsNullOrEmpty(member.PicAddress))
                    {
                        if (System.IO.File.Exists(System.Web.HttpContext.Current.Server.MapPath("~/" + member.PicAddress)))
                        {
                            System.IO.File.Delete(System.Web.HttpContext.Current.Server.MapPath("~/" + member.PicAddress));
                        }
                    }
                    return RedirectToAction("Index");
                }

                return RedirectToAction("Delete", new { id });
            }
            catch (MyExceptionHandler exp1)
            {
                try
                {
